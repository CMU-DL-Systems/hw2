class BroadcastTo(TensorOp):
    def __init__(self, shape):
        self.shape = shape

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.broadcast_to(a, self.shape)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        hs = node.inputs[0]
        hs_shape = hs.shape
        out_shape = self.shape
    
        # Collect axes of out_grad needed to be summed up: Extra axes in the beginning and unequal axes behind.
        i = 0
        sum_axes = []
        diff = 0
        if len(out_shape) > len(hs_shape):
            diff = len(out_shape) - len(hs_shape)
            while i < diff:
                sum_axes.append(i)
                i += 1

        while i < len(out_shape):
            if hs_shape[i - diff] != out_shape[i]:
                sum_axes.append(i)
            i += 1
        sum_axes = tuple(sum_axes)

        return summation(out_grad, sum_axes).reshape(hs_shape)
        ### END YOUR SOLUTION


def broadcast_to(a, shape):
    return BroadcastTo(shape)(a)


class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.sum(a, axis=self.axes)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        hs, = node.inputs

        # axes is None
        if not self.axes:
            return broadcast_to(out_grad, hs.shape)
        
        hs_shape = list(hs.shape)
        if self.axes:
            for i in self.axes:
                hs_shape[i] = 1
        hs_shape = tuple(hs_shape)
        return broadcast_to(out_grad.reshape(hs_shape), hs.shape)
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


